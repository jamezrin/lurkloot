// Syncs the built popup UI assets the landing page's interactive demo depends on.
// The demo imports the real popup React components (entrypoints/popup/app.tsx)
// and renders them inside a Shadow DOM with the extension's *compiled* CSS.
//
// Run after building the extension at the repo root:
//   pnpm build            # (repo root) -> .output/chrome-mv3
//   node site/scripts/sync-popup-ui.mjs
//
// Outputs (committed, so `pnpm deploy` stays self-contained):
//   site/src/popup-ui/popup.generated.css   (compiled popup CSS, :root -> :host)
//   site/public/_locales/<code>/messages.json
import { readFile, writeFile, mkdir, readdir, copyFile, rm } from "node:fs/promises";
import { resolve, join } from "node:path";
import { glob } from "node:fs/promises";

const repoRoot = resolve(import.meta.dirname, "..", "..");
const buildRoot = resolve(repoRoot, ".output/chrome-mv3");
const siteRoot = resolve(import.meta.dirname, "..");

// 1) Compiled popup CSS -> shadow-DOM-safe stylesheet.
let cssPath;
for await (const m of glob("assets/popup-*.css", { cwd: buildRoot })) cssPath = join(buildRoot, m);
if (!cssPath) throw new Error(`No compiled popup CSS in ${buildRoot}. Run \`pnpm build\` at the repo root first.`);
let css = await readFile(cssPath, "utf8");
// Theme tokens are emitted on `:root,:host`, but the dark-mode overrides target
// `:root` alone — which never matches inside a shadow tree. Rewriting every
// `:root` to `:host` makes all root-scoped vars/declarations apply to the shadow
// host (custom properties then inherit into the popup). Tailwind's `dark:`
// utilities are class+media based and already work inside the shadow root.
css = css.replaceAll(":root", ":host");
const cssOut = resolve(siteRoot, "src/popup-ui/popup.generated.css");
await mkdir(resolve(siteRoot, "src/popup-ui"), { recursive: true });
await writeFile(cssOut, `/* AUTO-GENERATED by scripts/sync-popup-ui.mjs — do not edit. */\n${css}`);
console.log(`Wrote ${cssOut} (${(css.length / 1024) | 0} kB)`);

// 2) Locale catalogs (the popup fetches /_locales/<code>/messages.json via the
//    mock getUrl, so they must be served at the site root).
const localesSrc = resolve(repoRoot, "public/_locales");
const localesOut = resolve(siteRoot, "public/_locales");
await rm(localesOut, { recursive: true, force: true });
for (const code of await readdir(localesSrc)) {
  await mkdir(join(localesOut, code), { recursive: true });
  await copyFile(join(localesSrc, code, "messages.json"), join(localesOut, code, "messages.json"));
}
console.log(`Copied ${(await readdir(localesSrc)).length} locale catalogs -> ${localesOut}`);

// 3) The popup header renders <img src={getUrl("/logo-ring.svg")}>; the mock
//    getUrl returns the path as-is, so serve the logo at the site root.
await copyFile(resolve(repoRoot, "public/logo-ring.svg"), resolve(siteRoot, "public/logo-ring.svg"));
console.log("Copied logo-ring.svg -> public/logo-ring.svg");
